public class Paso {

    private String step;
    private int timer;

    public Paso(String step) {
        this.step = step;

    }

    //GETTERS I SETTERS
    public String getStep() {
        return step;
    }

    public void setStep(String step) {
        this.step = step;
    }

    public int getTimer() {
        return timer;
    }

    public void setTimer(int timer) {
        this.timer = timer;
    }
}
